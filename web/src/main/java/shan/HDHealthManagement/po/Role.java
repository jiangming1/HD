package shan.HDHealthManagement.po;

/**
 * 角色实体类
 * @author 18732
 *
 */
public class Role {
	private Long id;
	private String name;
	private String memo;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
}
